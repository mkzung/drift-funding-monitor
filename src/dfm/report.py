def signals_as_html(signals: list[ArbSignal], *, title: str = "Arb Signals") -> str:
    head = _HTML_HEAD.replace("{title}", html.escape(title))
    body = [head, f"<h1>{html.escape(title)}</h1>"]
    if not signals:
        body.append('<p style="color:#8899b4">No signals above threshold.</p>')
    for s in signals:
        body.append('<div class="signal">')
        body.append(
            f'<h2>{html.escape(s.symbol)} — '
            f'<span class="badge red">SHORT {s.high_venue.value}</span>'
            f'<span class="badge green">LONG {s.low_venue.value}</span></h2>'
        )
        body.append(
            f'<div><span class="badge blue">{s.spread_bps_per_hour:.2f} bps/h</span>'
            f'<span class="badge gold">{s.annualized_spread_pct:.1f}% APR</span>'
            f'<span class="badge">confidence {s.confidence:.2f}</span></div>'
        )
        body.append(
            f'<div class="kv">Recommended size: ${s.recommended_size_usd:,.0f} · '
            f'breakeven: {s.breakeven_holding_hours:.1f}h · '
            f'{html.escape(s.reason)}</div>'
        )
        body.append("</div>")
    body.append(_HTML_TAIL)
    return "".join(body)


def backtest_as_html(result: BacktestResult, *, title: str = "Backtest") -> str:
    head = _HTML_HEAD.replace("{title}", html.escape(title))
    s = result.to_dict()
    body = [head, f"<h1>{html.escape(title)}</h1>"]
    body.append('<div class="signal">')
    body.append(
        f'<span class="badge blue">{int(s["n_trades"])} trades</span>'
        f'<span class="badge {"green" if s["total_pnl_usd"] >= 0 else "red"}">'
        f'PnL ${s["total_pnl_usd"]:,.0f}</span>'
        f'<span class="badge gold">win {s["win_rate"]:.0%}</span>'
        f'<span class="badge">Sharpe-proxy {s["sharpe_proxy"]:.2f}</span>'
    )
    body.append(f'<div class="kv">Avg PnL {s["avg_pnl_pct"]:.3f}% · '
                f'σ {s["pnl_pct_std"]:.3f}%</div></div>')
    if result.trades:
        body.append("<table><tr><th>opened</th><th>hours</th><th>size</th>"
                    "<th>funding</th><th>basis</th><th>fees</th>"
                    "<th>total</th><th>close</th></tr>")
        for t in result.trades:
            cls = "pnl-pos" if t.total_pnl_usd >= 0 else "pnl-neg"
            opened = datetime.fromtimestamp(t.opened_at, tz=timezone.utc).strftime("%Y-%m-%d %H:%M")
            body.append(
                f"<tr><td>{opened}</td><td>{t.hours_held:.1f}</td>"
                f"<td>${t.entry_size_usd:,.0f}</td>"
                f"<td>${t.funding_pnl_usd:,.2f}</td>"
                f"<td>${t.basis_pnl_usd:,.2f}</td>"
                f"<td>${t.fees_usd:,.2f}</td>"
                f'<td class="{cls}">${t.total_pnl_usd:,.2f}</td>'
                f"<td>{html.escape(t.reason_close)}</td></tr>"
            )
        body.append("</table>")
    body.append(_HTML_TAIL)
    return "".join(body)
