"""JSON / Markdown / HTML reporting for arb signals and backtest results."""

from __future__ import annotations

import html
import json
from dataclasses import asdict
from datetime import datetime, timezone
from typing import Any

from .backtest import BacktestResult
from .risk import RiskDetectorResult
from .signals import ArbSignal


def signals_as_json(signals: list[ArbSignal], *, indent: int = 2) -> str:
    return json.dumps([asdict(s) for s in signals], indent=indent, default=str)


def risks_as_json(risks: list[RiskDetectorResult], *, indent: int = 2) -> str:
    return json.dumps([asdict(r) for r in risks], indent=indent, default=str)


def backtest_as_json(result: BacktestResult, *, indent: int = 2) -> str:
    payload: dict[str, Any] = {
        "summary": result.to_dict(),
        "trades": [asdict(t) for t in result.trades],
    }
    return json.dumps(payload, indent=indent, default=str)


def signals_as_markdown(signals: list[ArbSignal], *, title: str = "Arb Signals") -> str:
    lines = [f"# {title}", ""]
    if not signals:
        lines.append("_No signals above threshold._")
        return "\n".join(lines)
    for s in signals:
        lines.append(f"## {s.symbol} — {s.high_venue.value} short / {s.low_venue.value} long")
        lines.append("")
        lines.append(f"- **Spread:** {s.spread_bps_per_hour:.2f} bps/h "
                     f"({s.annualized_spread_pct:.1f}% APR)")
        lines.append(f"- **Recommended size:** ${s.recommended_size_usd:,.0f}")
        lines.append(f"- **Breakeven:** {s.breakeven_holding_hours:.1f}h")
        lines.append(f"- **Confidence:** {s.confidence:.2f}")
        lines.append(f"- **Why:** {s.reason}")
        lines.append("")
    return "\n".join(lines)


def backtest_as_markdown(result: BacktestResult, *, title: str = "Backtest") -> str:
    lines = [f"# {title}", ""]
    s = result.to_dict()
    lines.append(f"- **Trades:** {int(s['n_trades'])}")
    lines.append(f"- **Total PnL:** ${s['total_pnl_usd']:,.2f}")
    lines.append(f"- **Win rate:** {s['win_rate']:.1%}")
    lines.append(f"- **Avg PnL %:** {s['avg_pnl_pct']:.3f}%")
    lines.append(f"- **PnL % stdev:** {s['pnl_pct_std']:.3f}%")
    lines.append(f"- **Sharpe proxy:** {s['sharpe_proxy']:.2f}")
    lines.append("")
    if result.trades:
        lines.append("| open | hours | size | funding | basis | fees | total | reason |")
        lines.append("|---:|---:|---:|---:|---:|---:|---:|---|")
        for t in result.trades:
            opened = datetime.fromtimestamp(t.opened_at, tz=timezone.utc).strftime("%Y-%m-%d %H:%M")
            lines.append(
                f"| {opened} | {t.hours_held:.1f} | ${t.entry_size_usd:,.0f} "
                f"| ${t.funding_pnl_usd:,.2f} | ${t.basis_pnl_usd:,.2f} | ${t.fees_usd:,.2f} "
                f"| ${t.total_pnl_usd:,.2f} | {t.reason_close} |"
            )
    return "\n".join(lines)


